def _species_search_pick(game_path):
    """Species search-and-pick for learnset editing.

    Returns a SPECIES_* constant, or None.
    """
    from torch.pickers import _search_pick
    species = load_species(game_path)
    skip = {"SPECIES_NONE", "SPECIES_EGG"}
    filtered = [(n, c) for n, c in species if n not in skip]
    return _search_pick(filtered, "Species", prefix_strip="SPECIES_")


def learnset_editor_menu(game_path, settings=None, proj_name=None):
    """Main entry point for the Learnset Editor.

    Called from ``torch learnsets`` or main menu.
    """
    # Version gate: vanilla pokeemerald doesn't use this format
    try:
        from torch.expansion_compat import detect_expansion_version
        version = detect_expansion_version(game_path)
        if version is None:
            print()
            print(f"  {DIM}Learnset Editor requires pokeemerald-expansion.{RST}")
            print(f"  {DIM}Vanilla pokeemerald uses a different learnset format.{RST}")
            input(f"\n  {DIM}Press Enter to return{RST} > ")
            return
    except ImportError:
        pass

    while True:
        print()
        print(f"  {WHITE}TORCH{RST} {DIM}-- Learnset Editor{RST}")
        print()
        print(f"  {DIM}Search for a Pokemon to view and edit its learnsets.{RST}")
        print()
        species = _species_search_pick(game_path)
        if not species:
            return
        _learnset_view(game_path, species, settings)
