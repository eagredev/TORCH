def _build_species_list(game_path):
    """Build a lightweight species list with learnset counts.

    Returns list of dicts: {name, display, level_up_count, egg_count, teachable_count}.
    """
    species_raw = load_species(game_path)
    if not species_raw:
        return []

    # Filter to real species only
    skip = {"SPECIES_NONE", "SPECIES_EGG", "SPECIES_UNOWN"}
    skip_prefixes = ("SPECIES_OLD_UNOWN",)
    entries = []
    for name, _comment in species_raw:
        if name in skip:
            continue
        if any(name.startswith(p) for p in skip_prefixes):
            continue
        entries.append(name)

    # Pre-load egg moves file once
    egg_path = os.path.join(game_path, _EGG_MOVES_FILE)
    egg_lines = []
    if os.path.isfile(egg_path):
        try:
            with open(egg_path, encoding="utf-8", errors="replace") as f:
                egg_lines = f.readlines()
        except OSError:
            pass

    # Pre-load teachable file once
    teach_path = os.path.join(game_path, _TEACHABLE_FILE)
    teach_lines = []
    if os.path.isfile(teach_path):
        try:
            with open(teach_path, encoding="utf-8", errors="replace") as f:
                teach_lines = f.readlines()
        except OSError:
            pass

    result = []
    for species in entries:
        array_name = _species_to_array_name(species)
        display = _species_display(species)

        # Count level-up (search file, parse)
        lu_file = _find_level_up_file(game_path, array_name)
        lu_count = 0
        if lu_file:
            try:
                with open(lu_file, encoding="utf-8", errors="replace") as f:
                    lu_lines = f.readlines()
                lu_count = len(_parse_level_up_learnset(lu_lines, array_name))
            except OSError:
                pass

        # Count egg moves from pre-loaded lines
        egg_count = len(_count_moves_in_lines(egg_lines, array_name, "EggMoveLearnset"))

        # Count teachable from pre-loaded lines
        teach_count = len(_count_moves_in_lines(teach_lines, array_name, "TeachableLearnset"))

        result.append({
            "name": species,
            "display": display,
            "level_up_count": lu_count,
            "egg_count": egg_count,
            "teachable_count": teach_count,
        })
    return result


def _count_moves_in_lines(lines, array_name, suffix):
    """Count MOVE_ entries in a named array within pre-loaded lines."""
    target = f"s{array_name}{suffix}"
    in_array = False
    moves = []
    for line in lines:
        if not in_array:
            if target in line and "=" in line and "{" in line:
                in_array = True
            continue
        if _ARRAY_END.match(line):
            break
        stripped = line.strip().rstrip(",").strip()
        if stripped == "MOVE_UNAVAILABLE":
            break
        if stripped.startswith("MOVE_"):
            moves.append(stripped)
    return moves


# ---------------------------------------------------------------------------
# Move picker (search-and-pick from all moves)
# ---------------------------------------------------------------------------

def _pick_move(game_path):
    """Interactive move search-and-pick.

    Uses pickers._search_pick to search all MOVE_* constants.
    Returns the selected move constant string, or None.
    """
    from torch.pickers import _search_pick
    moves = parse_defines(os.path.join(game_path, HEADER_MOVES), "MOVE_")
    # Filter out system moves
    skip = {"MOVE_NONE", "MOVE_UNAVAILABLE", "MOVE_COUNT"}
    filtered = [(n, c) for n, c in moves if n not in skip]
    return _search_pick(filtered, "Move", prefix_strip="MOVE_")


