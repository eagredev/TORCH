def _load_all_learnsets(game_path, array_name):
    """Load all three learnset types for a species.

    Returns (lu_moves, egg_moves, teachable).
    """
    lu_file = _find_level_up_file(game_path, array_name)
    lu_moves = []
    if lu_file:
        try:
            with open(lu_file, encoding="utf-8", errors="replace") as f:
                lu_lines = f.readlines()
            lu_moves = _parse_level_up_learnset(lu_lines, array_name)
        except OSError:
            pass
    egg_moves = _parse_egg_moves(game_path, array_name)
    teachable = _parse_teachable_learnset(game_path, array_name)
    return lu_moves, egg_moves, teachable


def _render_learnset_view(display, lu_moves, egg_moves, teachable):
    """Print the combined learnset view for a species."""
    print()
    print(f"  {WHITE}{display}{RST} {DIM}Learnsets{RST}")
    print(BAR)
    print()
    print(f"  {CYAN}Level-Up Moves ({len(lu_moves)}):{RST}")
    if lu_moves:
        sorted_lu = sorted(lu_moves, key=lambda x: (x[0], x[1]))
        for level, move in sorted_lu:
            print(f"    Lv {level:>2}  {WHITE}{_format_move_name(move)}{RST}")
    else:
        print(f"    {DIM}(none){RST}")
    print()
    print(f"  {CYAN}Egg Moves ({len(egg_moves)}):{RST}")
    if egg_moves:
        for move in egg_moves:
            print(f"    {WHITE}{_format_move_name(move)}{RST}")
    else:
        print(f"    {DIM}(none){RST}")
    print()
    if teachable:
        preview = ", ".join(_format_move_name(m) for m in teachable[:8])
        if len(teachable) > 8:
            preview += ", ..."
        print(f"  {CYAN}Teachable ({len(teachable)}, read-only):{RST}  {DIM}{preview}{RST}")
    else:
        print(f"  {CYAN}Teachable (0, read-only){RST}")


def _learnset_view(game_path, species_const, settings):
    """Show all learnset types for a species and offer editing."""
    array_name = _species_to_array_name(species_const)
    display = _species_display(species_const)

    while True:
        lu_moves, egg_moves, teachable = _load_all_learnsets(game_path, array_name)
        _render_learnset_view(display, lu_moves, egg_moves, teachable)

        print()
