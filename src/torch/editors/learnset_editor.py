def _find_level_up_file(game_path, species_name):
    """Find which gen_*.h file contains this species' level-up learnset.

    *species_name* is the PascalCase array name fragment (e.g. ``Bulbasaur``).
    Returns the full path, or None.
    """
    lu_dir = os.path.join(game_path, _LEVEL_UP_DIR)
    if not os.path.isdir(lu_dir):
        return None
    target = f"s{species_name}LevelUpLearnset"
    for fname in sorted(os.listdir(lu_dir)):
        if not fname.endswith(".h"):
            continue
        fpath = os.path.join(lu_dir, fname)
        try:
            with open(fpath, encoding="utf-8", errors="replace") as f:
                for line in f:
                    if target in line:
                        return fpath
        except OSError:
            continue
    return None


