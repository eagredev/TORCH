# Parsers
# ---------------------------------------------------------------------------

def _find_array_bounds(lines, pattern):
    """Find start and end line indices for a C array matching *pattern*.

    *pattern* is a compiled regex that matches the array declaration line
    and captures the species name in group 1.

    Returns (start_idx, end_idx, species_match) or (None, None, None).
    start_idx = the line with ``= {``, end_idx = the line with ``};``.
    """
    for i, line in enumerate(lines):
        m = pattern.match(line)
        if m:
            for j in range(i + 1, len(lines)):
                if _ARRAY_END.match(lines[j]):
                    return i, j, m.group(1)
    return None, None, None


def _find_named_array_bounds(lines, array_name_pattern):
    """Find start/end for a specific named array.

    *array_name_pattern* is a compiled regex matching the full declaration.
    Returns (start_idx, end_idx) or (None, None).
    """
    for i, line in enumerate(lines):
        if array_name_pattern.match(line):
            for j in range(i + 1, len(lines)):
                if _ARRAY_END.match(lines[j]):
                    return i, j
    return None, None


def _parse_level_up_learnset(lines, species_name):
    """Extract level-up moves from file lines for a given species.

    Returns list of (level, move_constant) tuples, or empty list.
    """
    target = f"s{species_name}LevelUpLearnset"
    in_array = False
    moves = []
    for line in lines:
        if not in_array:
            if target in line and "=" in line and "{" in line:
                in_array = True
            continue
        if _ARRAY_END.match(line):
            break
        if "LEVEL_UP_END" in line:
            break
        m = _LEVEL_UP_MOVE_RE.search(line)
        if m:
            moves.append((int(m.group(1)), m.group(2)))
    return moves


def _parse_egg_moves(game_path, species_name):
    """Extract egg moves for a species from egg_moves.h.

    Returns list of move constant strings, or empty list.
    """
    egg_path = os.path.join(game_path, _EGG_MOVES_FILE)
    if not os.path.isfile(egg_path):
        return []
    target = f"s{species_name}EggMoveLearnset"
    try:
        with open(egg_path, encoding="utf-8", errors="replace") as f:
            lines = f.readlines()
    except OSError:
        return []
    in_array = False
    moves = []
    for line in lines:
        if not in_array:
            if target in line and "=" in line and "{" in line:
                in_array = True
            continue
        if _ARRAY_END.match(line):
            break
        stripped = line.strip().rstrip(",").strip()
        if stripped == "MOVE_UNAVAILABLE":
            break
        if stripped.startswith("MOVE_"):
            moves.append(stripped)
    return moves


def _parse_teachable_learnset(game_path, species_name):
    """Extract teachable moves for a species (read-only).

    Returns list of move constant strings, or empty list.
    """
