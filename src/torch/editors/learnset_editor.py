"""Learnset Editor — browse and edit Pokemon level-up and egg move learnsets.

Provides a species-first browser: pick a Pokemon, view all its learnable moves,
then add/remove/reorder entries in level-up and egg move lists.

Teachable learnsets (TM/tutor) are displayed read-only since they are
auto-generated by make_teachables.py.

CLI entry: ``torch learnsets``
"""
# TORCH_MODULE: Learnset Editor
# TORCH_GROUP: Data Editors
import os
import re

from torch.colours import GOLD, WHITE, CYAN, DIM, RST, GREEN, RED, BAR
from torch.gamedata import HEADER_MOVES, HEADER_SPECIES, parse_defines, load_species
from torch.ui import _k, clear_screen

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

_LEVEL_UP_DIR = os.path.join("src", "data", "pokemon", "level_up_learnsets")
_EGG_MOVES_FILE = os.path.join("src", "data", "pokemon", "egg_moves.h")
_TEACHABLE_FILE = os.path.join("src", "data", "pokemon", "teachable_learnsets.h")

_LEVEL_UP_MOVE_RE = re.compile(
    r'LEVEL_UP_MOVE\(\s*(\d+)\s*,\s*(MOVE_\w+)\s*\)'
)
_ARRAY_START_LEVELUP = re.compile(
    r'^static\s+const\s+struct\s+LevelUpMove\s+s(\w+)LevelUpLearnset\[\]\s*=\s*\{'
)
_ARRAY_START_EGG = re.compile(
    r'^static\s+const\s+u16\s+s(\w+)EggMoveLearnset\[\]\s*=\s*\{'
)
_ARRAY_START_TEACHABLE = re.compile(
    r'^static\s+const\s+u16\s+s(\w+)TeachableLearnset\[\]\s*=\s*\{'
)
_ARRAY_END = re.compile(r'^\s*\};\s*$')


# ---------------------------------------------------------------------------
# Formatting helpers
# ---------------------------------------------------------------------------

def _format_move_name(move_const):
    """Convert ``MOVE_VINE_WHIP`` to ``Vine Whip``."""
    if not move_const or not move_const.startswith("MOVE_"):
        return move_const or ""
    return move_const[5:].replace("_", " ").title()


def _species_display(species_const):
    """Convert ``SPECIES_BULBASAUR`` to ``Bulbasaur``."""
    if not species_const or not species_const.startswith("SPECIES_"):
        return species_const or ""
    return species_const[8:].replace("_", " ").title()


def _species_to_array_name(species_const):
    """Convert ``SPECIES_BULBASAUR`` to ``Bulbasaur`` (PascalCase for array name).

    Handles multi-word names: ``SPECIES_MR_MIME`` -> ``MrMime``.
    """
    if not species_const or not species_const.startswith("SPECIES_"):
        return species_const or ""
    raw = species_const[8:]
    return "".join(word.capitalize() for word in raw.split("_"))


