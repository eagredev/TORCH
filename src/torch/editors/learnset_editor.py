def _lu_add(game_path, working):
    """Add a move to the level-up working list."""
    new_move = _pick_move(game_path)
    if not new_move:
        return
    raw = input(f"  Level (1-100) > ").strip()
    try:
        level = int(raw)
        if 1 <= level <= 100:
            working.append((level, new_move))
            print(f"  Added {_format_move_name(new_move)} at Lv {level}.")
        else:
            print(f"  {DIM}Level must be 1-100.{RST}")
    except ValueError:
        print(f"  {DIM}Invalid level.{RST}")


def _lu_delete(working):
    """Delete a move from the level-up working list."""
    if not working:
        return
    raw = input(f"  Delete # (1-{len(working)}) > ").strip()
    try:
        idx = int(raw) - 1
        if 0 <= idx < len(working):
            removed = working.pop(idx)
            print(f"  Removed {_format_move_name(removed[1])}.")
        else:
            print(f"  {DIM}Pick 1-{len(working)}.{RST}")
    except ValueError:
        print(f"  {DIM}Invalid number.{RST}")


def _lu_edit_level(working):
    """Edit the level of a move in the working list."""
    if not working:
        return
    raw = input(f"  Edit level for # (1-{len(working)}) > ").strip()
    try:
        idx = int(raw) - 1
        if 0 <= idx < len(working):
            new_lvl = input(f"  New level (1-100) > ").strip()
            lvl = int(new_lvl)
            if 1 <= lvl <= 100:
                working[idx] = (lvl, working[idx][1])
                print(f"  Updated to Lv {lvl}.")
            else:
                print(f"  {DIM}Level must be 1-100.{RST}")
        else:
            print(f"  {DIM}Pick 1-{len(working)}.{RST}")
    except ValueError:
        print(f"  {DIM}Invalid input.{RST}")


def _lu_replace(game_path, working):
    """Replace a move in the level-up working list."""
    if not working:
        return
    raw = input(f"  Replace # (1-{len(working)}) > ").strip()
    try:
        idx = int(raw) - 1
        if 0 <= idx < len(working):
            new_move = _pick_move(game_path)
            if new_move:
                working[idx] = (working[idx][0], new_move)
                print(f"  Replaced with {_format_move_name(new_move)}.")
        else:
            print(f"  {DIM}Pick 1-{len(working)}.{RST}")
    except ValueError:
        print(f"  {DIM}Invalid number.{RST}")


def _edit_level_up(game_path, species_name, moves):
    """Interactive level-up move editor.

    *moves*: list of (level, move_constant) tuples (mutable copy).
    Returns the edited list, or None if cancelled.
    """
    display = _species_display("SPECIES_" + species_name.upper())
    working = list(moves)

    while True:
        working.sort(key=lambda x: (x[0], x[1]))
        print()
        print(f"  {WHITE}{display}{RST} {DIM}-- Level-Up Moves{RST}")
        print()
        if working:
            for i, (level, move) in enumerate(working, 1):
                print(f"    {GOLD}{i:>2}.{RST} Lv {level:>2}  {WHITE}{_format_move_name(move):<20}{RST}  {DIM}({move}){RST}")
        else:
            print(f"    {DIM}(no moves){RST}")
        print()
        print(f"  {_k('a')} Add   {_k('d')} Delete   {_k('e')} Edit level   {_k('r')} Replace move")
        print(f"  {_k('c')} Confirm (save)   {_k('q')} Cancel")
        print()
        choice = input(f"  {GOLD}>{RST} ").strip().lower()

        if choice == "q":
            return None
        elif choice == "c":
            return working
        elif choice == "a":
            _lu_add(game_path, working)
        elif choice == "d":
            _lu_delete(working)
        elif choice == "e":
            _lu_edit_level(working)
        elif choice == "r":
            _lu_replace(game_path, working)


