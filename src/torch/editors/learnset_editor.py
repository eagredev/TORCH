def _format_level_up_array(species_name, moves):
    """Format a level-up learnset as C source lines.

    *moves*: list of (level, move_constant) tuples.
    Returns list of strings (each ending with newline).
    """
    sorted_moves = sorted(moves, key=lambda x: (x[0], x[1]))
    lines = [f"static const struct LevelUpMove s{species_name}LevelUpLearnset[] = {{\n"]
    for level, move in sorted_moves:
        lines.append(f"    LEVEL_UP_MOVE({level:2d}, {move}),\n")
    lines.append("    LEVEL_UP_END\n")
    lines.append("};\n")
    return lines


def _format_egg_move_array(species_name, moves):
    """Format an egg move learnset as C source lines.

    *moves*: list of move constant strings.
    Returns list of strings (each ending with newline).
    """
    lines = [f"static const u16 s{species_name}EggMoveLearnset[] = {{\n"]
    for move in moves:
        lines.append(f"    {move},\n")
    lines.append("    MOVE_UNAVAILABLE,\n")
    lines.append("};\n")
    return lines


def _write_level_up_learnset(game_path, species_name, moves):
    """Write an edited level-up learnset back to the correct gen_*.h file.

    Returns True on success, False on failure.
    """
    lu_file = _find_level_up_file(game_path, species_name)
    if not lu_file:
        print(f"  {RED}Could not find level-up learnset file for {species_name}.{RST}")
        return False
    try:
        with open(lu_file, encoding="utf-8", errors="replace") as f:
            lines = f.readlines()
    except OSError as e:
        print(f"  {RED}Could not read {lu_file}: {e}{RST}")
        return False

    target_re = re.compile(
        r'^static\s+const\s+struct\s+LevelUpMove\s+s'
        + re.escape(species_name)
        + r'LevelUpLearnset\[\]\s*=\s*\{'
    )
    start, end = _find_named_array_bounds(lines, target_re)
    if start is None:
        print(f"  {RED}Could not find array bounds for {species_name} level-up learnset.{RST}")
        return False

    new_array = _format_level_up_array(species_name, moves)
    lines[start:end + 1] = new_array

    try:
        with open(lu_file, "w", encoding="utf-8") as f:
            f.writelines(lines)
    except OSError as e:
        print(f"  {RED}Could not write {lu_file}: {e}{RST}")
        return False

    print(f"  {GREEN}Saved level-up learnset for {species_name} ({len(moves)} moves).{RST}")
    return True


def _write_egg_moves(game_path, species_name, moves):
    """Write edited egg moves back to egg_moves.h.

    Returns True on success, False on failure.
    """
    egg_path = os.path.join(game_path, _EGG_MOVES_FILE)
    if not os.path.isfile(egg_path):
        print(f"  {RED}egg_moves.h not found.{RST}")
        return False
    try:
        with open(egg_path, encoding="utf-8", errors="replace") as f:
            lines = f.readlines()
    except OSError as e:
        print(f"  {RED}Could not read egg_moves.h: {e}{RST}")
        return False

    target_re = re.compile(
        r'^static\s+const\s+u16\s+s'
        + re.escape(species_name)
        + r'EggMoveLearnset\[\]\s*=\s*\{'
    )
    start, end = _find_named_array_bounds(lines, target_re)
    if start is None:
        print(f"  {RED}Could not find array bounds for {species_name} egg moves.{RST}")
        return False

    new_array = _format_egg_move_array(species_name, moves)
    lines[start:end + 1] = new_array

    try:
        with open(egg_path, "w", encoding="utf-8") as f:
            f.writelines(lines)
    except OSError as e:
        print(f"  {RED}Could not write egg_moves.h: {e}{RST}")
        return False

    print(f"  {GREEN}Saved egg moves for {species_name} ({len(moves)} moves).{RST}")
    return True


