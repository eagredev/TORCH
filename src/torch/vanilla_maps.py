def get_vanilla_map_names(expansion_version=None):
    """Return the frozenset of vanilla map folder names.

    Args:
        expansion_version: tuple (major, minor, patch) from
            detect_expansion_version(), or None for vanilla pokeemerald
            (no expansion).

    Returns:
        frozenset of PascalCase map folder names that are vanilla.
        For base pokeemerald / expansion up to v1.14.x, returns _VANILLA_BASE.
        For newer versions, returns the union with any version-gated additions.
    """
    if not expansion_version or not _VERSION_ADDITIONS:
        return _VANILLA_BASE

    result = _VANILLA_BASE
    for min_ver, additions in _VERSION_ADDITIONS:
        if expansion_version >= min_ver and additions:
            result = result | additions
    return result
